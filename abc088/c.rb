require 'prime'

c_ary = []

3.times do |_i|
  c_ary << gets.split.map(&:to_i)
end

a1 = 0
b1 = c_ary[0][0] - a1
b2 = c_ary[0][1] - a1
b3 = c_ary[0][2] - a1

a2 = c_ary[1][0] - b1
a3 = c_ary[2][0] - b1

if c_ary[1][1] == a2 + b2 && c_ary[1][2] == a2 + b3 && c_ary[2][1] == a3 + b2 && c_ary[2][2] == a3 + b3
  puts 'Yes'
else
  puts 'No'
end
