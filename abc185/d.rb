require 'prime'

N, M = gets.chomp.split.map(&:to_i)
AS = [0] + gets.chomp.split.map(&:to_i).sort + [N + 1]

arr = AS.each_cons(2).to_a.map { |a, b| b - a - 1 }.reject(&:zero?)
size = arr.min

ans = 0
arr.each do |a|
  ans += (a + size - 1) / size
end
puts ans
