=begin
## 問題文
キーエンスには世界各地にN個の拠点があり、1からNまでの番号が付けられています。
拠点iにはWi​人の社員が所属しており、世界標準時で0時のとき拠点iはXi​時です。
あなたはキーエンス全社で1時間の会議を開きたいです。
各社員は、会議の開催時間帯が所属する拠点における9:00-18:00の時間帯に完全に含まれる場合にのみ会議に参加できます。
なるべく多くの社員が参加できるように会議の開催時間帯を決めるとき、会議に参加できる社員の数の最大値を求めてください。

## 制約
1≤N≤1000
1≤Wi​≤10**6
0≤Xi​<24
入力は全て整数

## 入力入力は以下の形式で標準入力から与えられる。
N
W1​ X1​
W2​ X2​
⋮
WN​ XN​

## 出力会議に参加できる社員の数の最大値を出力せよ。

## 入力例 1
3
5 0
