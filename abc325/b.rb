require 'prime'

n = gets.to_i
w_x_ary = Array.new(n) { gets.split.map(&:to_i) }

times = w_x_ary.map do |w, x|
  [(x + 9) % 24, (x + 18) % 24, w]
end

ans = 0
24.times do |i|
  tmp = 0
  times.each do |start, finish, w|
    if start <= finish
      tmp += w if i >= start && i < finish
    elsif i >= start || i < finish
      tmp += w
    end
  end
  ans = [ans, tmp].max
end

puts ans
