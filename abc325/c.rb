=begin
## 問題文
H行W列のマス目の上に0個以上のセンサが配置されています。
上からi行目、左からj列目のマス目を(i,j)と表記します。
センサが配置されているマス目の情報は長さWの文字列S1​,S2​,…,SH​によって与えられ、Si​のj文字目が#のとき、またそのときに限り(i,j)にセンサが配置されています。
このセンサは上下左右斜めに隣接しているマス目に存在する他のセンサと連動し、一つのセンサとして動作します。
ただし、マス目(x,y)と(x**′,y**′)が上下左右斜めに隣接しているとは、max(∣x−x**′∣,∣y−y**′∣)=1であることを指します。
また、センサAとセンサBが連動し、センサAとセンサCが連動しているとき、センサBとセンサCも連動することに注意してください。
連動するセンサを一つのセンサと見なしたとき、このマス目の上にあるセンサの個数を求めてください。

## 制約
1≤H,W≤1000
H,W は整数
Si​ は各文字が # または . である長さ W の文字列

## 入力入力は以下の形式で標準入力から与えられる。
