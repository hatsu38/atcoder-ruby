require 'prime'

h, w = gets.chomp.split.map(&:to_i)

s_ary = Array.new(h) { gets.chomp }

ans = 0

h.times do |i|
  w.times do |j|
    next if s_ary[i][j] == '.'

    ans += 1

    queue = [[i, j]]
    until queue.empty?
      x, y = queue.shift

      [[x - 1, y], [x + 1, y], [x, y - 1], [x, y + 1],
       [x - 1, y - 1], [x - 1, y + 1], [x + 1, y - 1], [x + 1, y + 1]].each do |xx, yy|
        next if xx.negative? || xx >= h || yy.negative? || yy >= w
        next if s_ary[xx][yy] == '.'

        s_ary[xx][yy] = '.'
        queue << [xx, yy]
      end
    end
  end
end

puts ans
