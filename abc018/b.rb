require 'prime'

s = gets.chomp
n = gets.to_i
lr_ary = []
n.times do
  lr_ary << gets.split.map(&:to_i)
end

lr_ary.each do |lr|
  l = lr[0] - 1
  r = lr[1] - 1
  s[l..r] = s[l..r].reverse
end

puts s
