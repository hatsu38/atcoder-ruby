require 'prime'
n = gets.to_i
s = gets.chomp.bytes.map! { |b| b - 0x30 }

t = Array.new(10, 0)
s.each { |x| t[x] += 1 }

cnt = 0
(0..).each do |x|
  ss = (x * x).digits
  break if ss.size > n

  tt = Array.new(10, 0)
  tt[0] = n - ss.size
  ss.each { |x| tt[x] += 1 }

  cnt += 1 if tt == t
end

puts cnt
