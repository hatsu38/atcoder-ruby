require 'prime'

n = gets.to_i

graph = Array.new(n) { [] }
(n - 1).times do
  a, b = gets.split.map(&:to_i)
  graph[a - 1] << (b - 1)
  graph[b - 1] << (a - 1)
end

graph.each(&:sort!)

visited = Array.new(n, false)
ans = []

def dfs(v, visited, ans, graph)
  visited[v] = true
  ans << (v + 1)

  graph[v].each do |u|
    next if visited[u]

    dfs(u, visited, ans, graph)
    ans << (v + 1)
  end
end

dfs(0, visited, ans, graph)

puts ans.join(' ')
